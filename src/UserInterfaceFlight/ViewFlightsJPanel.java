    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        rightPanel.remove(this);
        Component[] componentArr = rightPanel.getComponents();
        Component comp = componentArr[componentArr.length - 1];
        
        if (comp instanceof ManageFlightJPanel) {
            ManageFlightJPanel mf = (ManageFlightJPanel) comp;
            mf.populateTable();
        }
        
        if (comp instanceof SearchFlightJPanel) {
            SearchFlightJPanel sf = (SearchFlightJPanel) comp;
            
        }
        
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        layout.previous(rightPanel);
        
    }//GEN-LAST:event_btnBackActionPerformed

