    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        txtFlightName.setEnabled(false);
        txtFlightNo.setEnabled(false);
        txtSource.setEnabled(false);
        txtDestination.setEnabled(false);
        txtPrice.setEnabled(false);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
        
        try {
                f.setFlightName(txtFlightName.getText());
                f.setFlightSource(txtSource.getText());
                f.setFlightDestination(txtDestination.getText());
                f.setFlightPrice(Float.parseFloat(txtPrice.getText()));
        
                JOptionPane.showMessageDialog(null, "Flight updated successfully!!");        
        
            }
            catch (NumberFormatException e)
            {
                 JOptionPane.showMessageDialog(null, "Please enter numeric values only!!");
                 //return;
            } 
    }//GEN-LAST:event_btnSaveActionPerformed

